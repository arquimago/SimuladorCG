int main() {
	int i,j,k,l;
	
	for(i=0;i<3;i++){
		for(j=0;j<3;j++){
			for(k=0;k<3;k++){
				for(l=0;l<3;l++){
					if (l<2)printf("%d, ", tabela[i][j][k][l]);
					else printf("%d\n", tabela[i][j][k][l]);
				}
				
				if(k<2) printf(",");
				else printf("\n");
			}
			if(j<2) printf(",");
			else printf("\n");
		}
		if(i<2) printf(",");
		else printf("\n");
	}
	printf("\n");
}

